number = int(input("Enter a number: "))
squareNumber = number * number
if number > 100:
    print("number is greater than 100")
if number < 100:
    print("number is less than 100")
if number == 100:
    print("number is equal to 100")
if number != 100:
    print("number is not equal to 100")
if squareNumber > 100:
    print(squareNumber, "is greater than 100")
if squareNumber < 100:
    print(squareNumber, "is less than 100")
if squareNumber == 100:
    print(squareNumber, "is equal to 100")
if squareNumber != 100:
    print(squareNumber, "is not equal to 100")
